package oop_00000110694_darren.week05

class MathHelper {
    fun hitungLuas(sisi: Int) : Int{
        return sisi * sisi
    }

    fun hitungLuas(panjang: Int, lebar: Int) : Int {
        return panjang * lebar
    }

    fun hitungLuas(jarijari: Double) : Double {
        return 3.14 * jarijari * jarijari
    }
}